export const DEFAULT_CATEGORY_ID = CATEGORY_CATALOG[0].id
export const DEFAULT_SUBCATEGORY_ID = CATEGORY_CATALOG[0].subcategories[0].id

export function getCategory(categoryId) {
  return CATEGORY_CATALOG.find((category) => category.id === categoryId) || CATEGORY_CATALOG[0]
}

export function getSubcategory(categoryId, subcategoryId) {
  const category = getCategory(categoryId)
  return category.subcategories.find((subcategory) => subcategory.id === subcategoryId) || category.subcategories[0]
}

export function getRandomWordPair(categoryId, subcategoryId) {
  const subcategory = getSubcategory(categoryId, subcategoryId)
  return subcategory.words[Math.floor(Math.random() * subcategory.words.length)]
}

export function getRandomCategorySelection() {
  const category = CATEGORY_CATALOG[
    Math.floor(Math.random() * CATEGORY_CATALOG.length)
  ]
  const subcategory = category.subcategories[
    Math.floor(Math.random() * category.subcategories.length)
  ]

  return { category, subcategory }
}
